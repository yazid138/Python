#!/bin/python3

import math

# Ambil input untuk mengisi nilai
a = int(input("Inputkan nilai a: "))
b = int(input("Inputkan nilai b: "))

# Menggunakan operator penjumlahan
c = a + b
print ("Hasil %d + %d = %d" % (a,b,c))

# Operator Pengurangan
c = a - b
print ("Hasil %d - %d = %d" % (a,b,c))

# Operator Perkalian
c = a * b
print ("Hasil %d * %d = %d" % (a,b,c))

# Operator Pembagian
c = a / b
print ("Hasil %d / %d = %d" % (a,b,c))

# Operator Sisa Bagi
c = a % b
print ("Hasil %d %% %d = %d" % (a,b,c))

# Operator Pangkat
c = a ** b
print ("Hasil %d ** %d = %d" % (a,b,c))